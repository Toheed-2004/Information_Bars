# ===================== HELPER FUNCTIONS =====================


def get_features_by_family(family: str) -> List[Dict]:
    """Get all features for a specific family."""
    return [f for f in FEATURES.values() if f["family"] == family]


def get_features_by_subfamily(family: str, subfamily: str) -> List[Dict]:
    """Get all features for a specific subfamily."""
    return [
        f for f in FEATURES.values() if f["family"] == family and f["subfamily"] == subfamily
    ]


def get_all_features() -> List[str]:
    """Get all feature names. O(1) operation."""
    return list(FEATURES.keys())


def validate_features(features: List[str]) -> Tuple[List[str], List[str]]:
    """Validate features against the registry. O(n) where n is number of features to validate."""
    valid, invalid = [], []

    for f in features:
        if f in FEATURES:  # O(1) lookup
            valid.append(f)
        else:
            invalid.append(f)

    return valid, invalid


def get_feature_info(feature_name: str) -> Dict[str, Any]:
    """Get full feature configuration. O(1) lookup."""
    if feature_name not in FEATURES:
        raise ValueError(f"Feature '{feature_name}' not found in registry")
    return FEATURES[feature_name]


def get_feature_by_id(feature_id: str) -> Dict[str, Any]:
    """Get feature by ID."""
    for feature in FEATURES.values():
        if feature["id"] == feature_id:
            return feature
    raise ValueError(f"Feature ID '{feature_id}' not found in registry")


def get_features_by_status(status: str) -> List[Dict]:
    """Get features by lifecycle status."""
    return [f for f in FEATURES.values() if f["lifecycle"]["status"] == status]


def get_features_by_domain(domain: str) -> List[Dict]:
    """Get features by domain type."""
    return [f for f in FEATURES.values() if f["metadata"]["domain"] == domain]


def get_stationary_required_features() -> List[Dict]:
    """Get features requiring stationarity."""
    return [f for f in FEATURES.values() if f["metadata"]["stationary_required"]]


def get_features_by_category(category: str) -> List[str]:
    """Backward compatibility: get feature names by family."""
    return [f["name"] for f in get_features_by_family(category)]


def get_feature_metadata(feature_name: str) -> Dict[str, str]:
    """Backward compatibility: get family and subfamily."""
    feature = get_feature_info(feature_name)
    return {"category": feature["family"], "subcategory": feature["subfamily"]}

def get_all_domains() -> list:
    """
    Get all unique domains from the FEATURES registry.
    Returns a sorted list of domains.
    """
    unique_domains = sorted({f["metadata"]["domain"] for f in FEATURES.values()})
    return unique_domains
